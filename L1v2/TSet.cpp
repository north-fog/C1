#include "TSet.h"

// Конструктор создания пустого множества заданной мощности
// mp - максимальная мощность 
TSet::TSet(int mp) : MaxPower(mp), BitFild(mp) {
    // Инициализация полей через список инициализации:
    // MaxPower = mp - максимальный возможный элемент множества
    // BitFild(mp) - создание битового поля размера mp (все биты = 0)
}

// Конструктор копирования
// s - исходное множество для копирования
TSet::TSet(const TSet& s): MaxPower(s.MaxPower), BitFild(s.BitFild) {
    // Копирование полей из объекта s:
    // MaxPower = s.MaxPower - копирование максимальной мощности
    // BitFild(s.BitFild) - копирование битового поля через конструктор копирования TBitField
}

// Конструктор преобразования из битового поля
// bf - битовое поле, на основе которого создается множество
TSet::TSet(const TBitField& bf): MaxPower(bf.GetLen()), BitFild(bf) {
    // Создание множества на основе существующего битового поля:
    // MaxPower = bf.GetLen() - мощность = длине битового поля
    // BitFild(bf) - инициализация битового поля через конструктор копирования
}

// Оператор преобразования типа - преобразование TSet в TBitField
// Возвращает копию битового поля множества
TSet::operator TBitField() {
    TBitField tmp(this->BitFild);  // Создание временной копии битового поля
    return tmp;                     // Возврат копии
}

// Добавление элемента в множество
// elem - элемент для добавления (должен быть в диапазоне [0, MaxPower-1])
void TSet::InsLem(const int elem) {
    BitFild.SetBit(elem);  // Установка соответствующего бита в 1
}

// Удаление элемента из множества
// elem - элемент для удаления
void TSet::DelLem(const int elem) {
    BitFild.ClrBit(elem);  // Сброс соответствующего бита в 0
}

// Проверка принадлежности элемента множеству
// elem - проверяемый элемент
// Возвращает: 1 - элемент принадлежит, 0 - не принадлежит
int TSet::isMember(const int elem) const {
    return BitFild.GetBit(elem);  // Получение значения бита (0 или 1)
}

// Оператор сравнения множеств на равенство
// s - множество для сравнения
// Возвращает: 1 - множества равны, 0 - не равны
int TSet::operator==(const TSet& s) {
    return (BitFild == s.BitFild);  // Сравнение битовых полей
}

// Оператор присваивания
// s - исходное множество
// Возвращает ссылку на текущий объект
TSet& TSet::operator=(const TSet& s) {
    MaxPower = s.MaxPower;  // Копирование максимальной мощности
    BitFild = s.BitFild;    // Копирование битового поля через оператор присваивания TBitField
    return *this;           // Возврат ссылки на текущий объект
}

// Оператор объединения множеств (теория множеств: A ? B)
// s - второе множество
// Возвращает новое множество - результат объединения
TSet TSet::operator+(const TSet& s) {
    TSet res(BitFild | s.BitFild);  // Побитовое ИЛИ битовых полей
    return res;                     // Возврат результата
}

// Оператор пересечения множеств (теория множеств: A ? B)
// s - второе множество
// Возвращает новое множество - результат пересечения
TSet TSet::operator*(const TSet& s) {
    TSet res(BitFild & s.BitFild);  // Побитовое И битовых полей
    return res;                     // Возврат результата
}

// Оператор дополнения множества (теория множеств: ¬A или A?)
// Возвращает новое множество - дополнение текущего до универсума
TSet TSet::operator~() {
    TSet res(~BitFild);  // Побитовое НЕ битового поля
    return res;          // Возврат результата
}